package api;

import java.util.List;

/**
 * Describes the result obtained upon computation of a remote {@link api.Task Task}
 * 
 * @author Manasa Chandrasekhar
 * @author Kowshik Prakasam
 */

public interface Result<T> {
	
	/**
	 * Sets the start time of the underlying computation
	 */
	void setStartTime(long time);
	
	/**
	 * @return Returns the start time of the underlying computation
	 */
	long getStartTime();
	
	/**
	 * Sets the end time of the underlying computation
	 */
	void setEndTime(long time);
	
	/**
	 * @return Returns the end time of the underlying computation
	 */
	long getEndTime();
	
	/**
	 * @return Returns the value computed by the underlying computation
	 */
	T getValue(); 	
	
	/**
	 * @return Returns sub tasks generated by the underlying computation using divide and conquer
	 */
	
	List<Task<T>> getSubTasks();


	
}
